// Helper functions

// Get all plants
export function getAllPlants(): PlantTypeInfo[] {
  return plantTypes;
}

// Get plants by category
export function getPlantsByCategory(category: string): PlantTypeInfo[] {
  return plantTypes.filter(plant => plant.category === category);
}

// Get plant by ID
export function getPlantById(id: string): PlantTypeInfo | undefined {
  return plantTypes.find(plant => plant.id === id);
}

// Search plants by name
export function searchPlants(query: string): PlantTypeInfo[] {
  if (!query || query.trim() === '') return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  return plantTypes.filter(plant => 
    plant.name.toLowerCase().includes(normalizedQuery) || 
    plant.scientificName.toLowerCase().includes(normalizedQuery)
  );
}